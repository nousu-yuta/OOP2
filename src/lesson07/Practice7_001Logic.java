package lesson07;

public class Practice7_001Logic {

    public static int add(int num1, int num2) {
        return num1 + num2;
    }

    public static int sub(int num1, int num2) {
        return num1 - num2;
    }

    public static int roundDown(float num) {
        return (int) Math.floor(num);
    }

}
